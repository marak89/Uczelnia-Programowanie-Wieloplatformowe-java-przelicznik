    private void jButton1_wczytajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_wczytajActionPerformed
    DefaultTableModel model = (DefaultTableModel) jTable1_zuzycie_paliwa.getModel();

    Boolean isOk = true;
        if(jTextField1_stan_licznika.getText().trim().equals("")){
            isOk = false;
            JOptionPane.showMessageDialog(this, "Nie podałeś stanu licznika!","Złe dane!", 2);
        }

        if(jTextField2_zatankowano.getText().trim().equals("")){
            isOk = false;
            JOptionPane.showMessageDialog(this, "Nie podałeś ilości paliwa!","Złe dane!", 2);
        }

        if(jTable1_zuzycie_paliwa.getSelectedRow() == -1){
            if(jTable1_zuzycie_paliwa.getRowCount() == 0){
                JOptionPane.showMessageDialog(this, "Tabela jest pusta!","Złe dane!", 2);
                isOk = false;
            } else {
                JOptionPane.showMessageDialog(this, "Musisz wybrać wpis!","Złe dane!", 2);
                isOk = false;
            }
        }

        if(isOk.equals(true)){
            String litrow = jTextField2_zatankowano.getText();
            String stanLicznika = jTextField1_stan_licznika.getText();
            
            
            String lastLicznik = "";
            double NewLicznik = 0;
            
            if(jTable1_zuzycie_paliwa.getRowCount() > 0){
                lastLicznik = model.getValueAt(jTable1_zuzycie_paliwa.getSelectedRow()-1, 0).toString();
                int lastInt = Integer.parseInt(lastLicznik);
                int newInt = Integer.parseInt(stanLicznika);
                NewLicznik = newInt - lastInt ;
                
            } else {
                NewLicznik = 0;
            }

            model.setValueAt(stanLicznika, jTable1_zuzycie_paliwa.getSelectedRow(), 0);
            model.setValueAt(litrow, jTable1_zuzycie_paliwa.getSelectedRow(), 1);
            model.setValueAt(NewLicznik, jTable1_zuzycie_paliwa.getSelectedRow(), 2);

            policzSpalanie();
            jLabel5_info_label.setText("Poprawiono wpis");
            jTextField2_zatankowano.setText("");
            jTextField1_stan_licznika.setText("");

        }

    }//GEN-LAST:event_jButton1_wczytajActionPerformed

    private void jButton_usun_wpisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_usun_wpisActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1_zuzycie_paliwa.getModel();
        Boolean isOk = true;
        if(jTable1_zuzycie_paliwa.getSelectedRow() == -1){
            if(jTable1_zuzycie_paliwa.getRowCount() == 0){
                JOptionPane.showMessageDialog(this, "Tabela jest pusta!","Złe dane!", 2);
                isOk = false;
            } else {
                JOptionPane.showMessageDialog(this, "Musisz wybrać wpis!","Złe dane!", 2);
                isOk = false;
            }
        }

        if(isOk.equals(true)){
            model.removeRow(jTable1_zuzycie_paliwa.getSelectedRow());
            policzSpalanie();
            jLabel5_info_label.setText("Usunięto wpis");
            jTextField2_zatankowano.setText("");
            jTextField1_stan_licznika.setText("");
        }
    }//GEN-LAST:event_jButton_usun_wpisActionPerformed

