    private void policzSpalanie(){
        jLabel4_srednie_zuzycie_paliwa.setText("Obliczam...");
        DefaultTableModel model = (DefaultTableModel) jTable1_zuzycie_paliwa.getModel();
        double srednia = 0;
        int i = 0;
        int tyleZer = 0;
        for (; i < jTable1_zuzycie_paliwa.getRowCount(); i++) {
            
            double przejechano = Double.parseDouble(model.getValueAt(i, 2).toString());
            if(przejechano != 0 ){
                double spalono = Double.parseDouble(model.getValueAt(i, 1).toString());
                double sredniaZPrzejazu = (spalono / przejechano)*100;
//                System.out.println("Średnia przejazdu " + i + " to " + srednia);
                srednia += sredniaZPrzejazu;
            } else {
                tyleZer += 1;
            }
        }
//        System.out.println(i);
//        System.out.println(srednia);
        srednia = srednia / (i - tyleZer);
//        System.out.println(srednia);
        if(srednia > 0 ){
            jLabel4_srednie_zuzycie_paliwa.setText(String.format("%.2f",srednia));
        } else {
            jLabel4_srednie_zuzycie_paliwa.setText("Za mało danych");
        }
        
    }
    
    private void jButton1_dodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_dodajActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1_zuzycie_paliwa.getModel();

        Boolean isOk = true;
        if(jTextField1_stan_licznika.getText().trim().equals("")){
            isOk = false;
            JOptionPane.showMessageDialog(this, "Nie podałeś stanu licznika!","Złe dane!", 2);
        }

        if(jTextField2_zatankowano.getText().trim().equals("")){
            isOk = false;
            JOptionPane.showMessageDialog(this, "Nie podałeś ilości paliwa!","Złe dane!", 2);
        }

        if(isOk.equals(true)){
            String litrow = jTextField2_zatankowano.getText();
            String stanLicznika = jTextField1_stan_licznika.getText();
            String lastLicznik = "";
            double NewLicznik = 0;
            
            if(jTable1_zuzycie_paliwa.getRowCount() > 0){
                lastLicznik = model.getValueAt(jTable1_zuzycie_paliwa.getRowCount()-1, 0).toString();
                int lastInt = Integer.parseInt(lastLicznik);
                int newInt = Integer.parseInt(stanLicznika);
                NewLicznik = newInt - lastInt ;
                
            } else {
                NewLicznik = 0;
            }
            model.addRow(new Object[]{stanLicznika,litrow,NewLicznik});

            policzSpalanie();
            jLabel5_info_label.setText("Dodano wpis");
            jTextField2_zatankowano.setText("");
            jTextField1_stan_licznika.setText("");

        }

    }//GEN-LAST:event_jButton1_dodajActionPerformed

